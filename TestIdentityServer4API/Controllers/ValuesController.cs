        [Authorize]
        public ActionResult<IEnumerable<string>> Get()
        {
            List<string> userClaims = new List<string>();

            // Get calling user information
            foreach(var claim in HttpContext.User.Claims){
                userClaims.Add($"{claim.Type}:{claim.Value}");
